    private void bloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bloginActionPerformed
        String query = "Select * from admin where username_admin='"+txtusername.getText()+"' and Password='"+String.valueOf(txtpass.getPassword())+"'";
        try{
            PreparedStatement st = conn.prepareStatement(query);
            ResultSet rs = st.executeQuery(query);
            if(rs.next()){
                if(txtusername.getText().equals(rs.getString("username_admin"))&&String.valueOf(txtpass.getPassword()).equals(rs.getString("Password"))){
                    LoginSession.setUserLogin(rs.getString("username_admin"));
                    JOptionPane.showMessageDialog(this, "          Login Sukses \nSelamat Datang "+rs.getString("username_admin"));
                    new formutama().setVisible(true);
                    this.setVisible(false);
                }
            }
        }catch(SQLException yusuf){
            JOptionPane.showMessageDialog(this, "Error "+yusuf);
        }
    }//GEN-LAST:event_bloginActionPerformed

